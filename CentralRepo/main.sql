CREATE FUNCTION SoLuong_KH_TrongThang (
	p_month INT;
	p_year INT
)
RETURNS INT
DETERMINISTIC
READS SQL DATA
BEGIN 
	DECLARE SLKH INT;
	SELECT COUNT(*) INTO SLKH
	FROM KHACHHANG
	WHERE MONTH(NGAYTAOTK) = p_month AND YEAR(NGAYTAOTK) = p_year;
	RETURN SLKH;
END //
DELIMITER;

